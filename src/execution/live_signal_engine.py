def _ibkr_bars_to_df(bars) -> pd.DataFrame:
    """Convert ib_insync BarDataList -> standard OHLCV DataFrame in ET."""
    if not bars:
        return pd.DataFrame()
    rows = []
    for b in bars:
        ts = b.date
        if isinstance(ts, str):
            ts = pd.Timestamp(ts)
        elif not isinstance(ts, pd.Timestamp):
            ts = pd.Timestamp(ts)
        rows.append({
            "Timestamp": ts,
            "Open":      float(b.open),
            "High":      float(b.high),
            "Low":       float(b.low),
            "Close":     float(b.close),
            "Volume":    float(b.volume) if b.volume >= 0 else 0.0,
        })
    df = pd.DataFrame(rows)
    df["Timestamp"] = pd.to_datetime(df["Timestamp"])

    # Localise to ET (IBKR returns US/Eastern for RTH bars when formatDate=1)
    if df["Timestamp"].dt.tz is None:
        df["Timestamp"] = df["Timestamp"].dt.tz_localize("America/New_York",
                                                          ambiguous="NaT",
                                                          nonexistent="NaT")
    else:
        df["Timestamp"] = df["Timestamp"].dt.tz_convert("America/New_York")

    df = df.dropna(subset=["Timestamp"])
    df = df.set_index("Timestamp").sort_index()
    return df


def _request_bars(ib: IB, market: str, bar_size: str,
                  duration: str = "2 D") -> pd.DataFrame:
    """Request historical IBKR bars. Returns empty DataFrame on failure."""
    if not IB_AVAILABLE:
        log.error("ib_insync not available")
        return pd.DataFrame()
    contract = make_contract(market)
    try:
        ib.qualifyContracts(contract)
    except Exception as e:
        log.warning("{m}: contract qualify failed -- {e}", m=market, e=e)

    try:
        bars = ib.reqHistoricalData(
            contract,
            endDateTime   = "",          # up to now
            durationStr   = duration,
            barSizeSetting= bar_size,
            whatToShow    = "TRADES",
            useRTH        = True,
            formatDate    = 1,           # exchange timezone string (ET for ES/NQ)
            timeout       = 30,
        )
        df = _ibkr_bars_to_df(bars)
        log.info("{m}: got {n} {bs} bars from IBKR", m=market, n=len(df), bs=bar_size)
        return df
    except Exception as e:
        log.error("{m}: IBKR historical data failed -- {e}", m=market, e=e)
        return pd.DataFrame()


